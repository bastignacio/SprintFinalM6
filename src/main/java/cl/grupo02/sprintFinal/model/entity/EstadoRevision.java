/**
 * Enumeración que representa los posibles estados de una revisión.
 * Los estados pueden ser:
 *     pendiente:La revisión está pendiente de evaluación.
 *     aprobada:La revisión ha sido aprobada.
 *    reprobada:La revisión ha sido reprobada.
 *
 * 
 * @author Ariel Alfaro, Bastian Muñoz, Bastian Espinosa, Joshua Montt, Nicolas Gajardo
 * @version 1.0
 */

public enum EstadoRevision {
    pendiente,
    aprobada,
    reprobada
}
